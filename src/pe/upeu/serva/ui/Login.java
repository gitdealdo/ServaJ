    private void login(){
        try {
            boolean rpta = false;
            if(!txtUsuario.getText().trim().equals("") && !txtClave.getText().trim().equals("")) {
                rpta = comprobarUsuario(QUERY_USUARIO + txtUsuario.getText() + "%'");
                if (rpta) {
                    this.setVisible(false);
                    home.setVisible(rpta);
                }else{
                    lblMensaje.setText("Usuario รณ clave incorrectos!");
                }           
            }else{
                JOptionPane.showMessageDialog(null, "Por favor llene los campos correctamente!");
            }   
        } catch (Exception e) {
            lblMensaje.setText("Usuario รณ clave incorrectos!");
        }
        
    }
